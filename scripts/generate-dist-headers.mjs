#!/usr/bin/env node
/**
 * Writes Cloudflare Pages `_headers` rules for the production `dist/` output.
 */

import { writeFileSync, statSync } from "node:fs";
import { join } from "node:path";
import { fileURLToPath } from "node:url";

const distDir = join(fileURLToPath(import.meta.url), "..", "..", "dist");

const HEADERS = `# Generated by scripts/generate-dist-headers.mjs — do not edit in dist/

# Fingerprinted build assets (Vite assets/[name]-[hash] output)
/assets/*
  Cache-Control: public, max-age=31536000, immutable, no-transform
  Vary: Accept-Encoding

# HTML documents
/
  Cache-Control: public, max-age=0, must-revalidate
  Vary: Accept-Encoding

/*.html
  Cache-Control: public, max-age=0, must-revalidate
  Vary: Accept-Encoding

# Public scripts copied from public/
/*.mjs
  Cache-Control: public, max-age=86400
  Vary: Accept-Encoding

# Web app manifest
/site.webmanifest
  Cache-Control: public, max-age=86400
  Vary: Accept-Encoding

# Icons and images
/*.png
  Content-Type: image/png
  Cache-Control: public, max-age=604800
/*.ico
  Content-Type: image/vnd.microsoft.icon
  Cache-Control: public, max-age=604800
`;

if (!statSync(distDir, { throwIfNoEntry: false })) {
  console.error("generate-dist-headers: dist/ not found. Run vite build first.");
  process.exit(1);
}

writeFileSync(join(distDir, "_headers"), HEADERS, "utf8");
console.log("generate-dist-headers: wrote dist/_headers");
